    private void lihatDetailBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lihatDetailBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = tabelSekolah.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this,
                    "Pilih sekolah terlebih dahulu!",
                    "Peringatan",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        // kalau suatu saat pakai sorter, aman-kan index:
        int modelRow = tabelSekolah.convertRowIndexToModel(selectedRow);
        Sekolah sekolahDipilih = daftarSekolah.get(modelRow);

        DialogManajemenSekolah dialog = new DialogManajemenSekolah(
                (java.awt.Frame) SwingUtilities.getWindowAncestor(this),
                true,
                sekolahDipilih
        );
        dialog.setLocationRelativeTo(this);
        dialog.setVisible(true);

        // refresh setelah dialog ditutup
        loadDataSekolah();
    }//GEN-LAST:event_lihatDetailBtnActionPerformed

