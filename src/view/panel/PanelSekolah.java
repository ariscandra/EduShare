    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = tabelSekolah.getSelectedRow();
        
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, 
                "Pilih sekolah yang ingin dihapus!", 
                "Peringatan", 
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // Ambil sekolah yang dipilih
        Sekolah sekolahTerpilih = daftarSekolah.get(selectedRow);
        
        // Konfirmasi hapus
        int konfirmasi = JOptionPane.showConfirmDialog(this,
            "Apakah Anda yakin ingin menghapus sekolah:\n" + 
            sekolahTerpilih.getNamaSekolah() + "?\n\n" +
            "Perhatian: Sekolah yang memiliki data kebutuhan atau dokumen tidak bisa dihapus.",
            "Konfirmasi Hapus",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.WARNING_MESSAGE);
            
        if (konfirmasi == JOptionPane.YES_OPTION) {
            try {
                // Hapus sekolah menggunakan method yang benar dari SekolahService
                SekolahService.OperationResult result = 
                    sekolahService.deleteSekolah(sekolahTerpilih.getIdSekolah());
                
                if (result.isSuccess()) {
                    JOptionPane.showMessageDialog(this,
                        result.getMessage(),
                        "Sukses",
                        JOptionPane.INFORMATION_MESSAGE);
                        
                    // Refresh data
                    loadDataSekolah();
                } else {
                    JOptionPane.showMessageDialog(this,
                        result.getMessage(),
                        "Gagal",
                        JOptionPane.ERROR_MESSAGE);
                }
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,
                    "Error menghapus sekolah: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

    /**
     * Setup table dengan kolom yang sesuai
     */
    private void setupTable() {
        String[] columns = {
            "Nama Sekolah",
            "NPSN",
            "Jenis Sekolah",
            "Alamat",
            "Kecamatan",
            "Kabupaten",
            "Status Verifikasi"
        };

        tableModel = new DefaultTableModel(columns, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // semua kolom tidak bisa diedit
            }
        };

        tabelSekolah.setModel(tableModel);
        tabelSekolah.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        tabelSekolah.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        // Atur lebar kolom biar rapi
        int[] widths = {180, 100, 90, 200, 130, 130, 130};
        for (int i = 0; i < widths.length; i++) {
            tabelSekolah.getColumnModel().getColumn(i).setPreferredWidth(widths[i]);
        }
    }


    
    /**
     * Load data sekolah dari database
     */
    private void loadDataSekolah() {
        try {
            tableModel.setRowCount(0);
            daftarSekolah = sekolahService.getAllSekolah();

            for (Sekolah sekolah : daftarSekolah) {
                Object[] row = {
                    sekolah.getNamaSekolah(),
                    sekolah.getNpsn(),
                    sekolah.getKategori() != null ? sekolah.getKategori().toString() : "-",
                    sekolah.getAlamatSekolah(),
                    sekolah.getKecamatan(),
                    sekolah.getKabupaten(),
                    sekolah.getStatusVerifikasi() != null ? sekolah.getStatusVerifikasi() : "-"
                };
                tableModel.addRow(row);
            }

            // Update label jumlah sekolah
            jLabel14.setText("Daftar Sekolah (" + daftarSekolah.size() + " sekolah terdaftar)");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                "Error memuat data sekolah: " + e.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }

    

