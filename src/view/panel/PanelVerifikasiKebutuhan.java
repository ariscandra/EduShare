    private void setupTable() {
        tableModel = new DefaultTableModel(
                new Object[]{"ID", "Sekolah", "Nama Kebutuhan", "Jumlah", "Prioritas", "Status"}, 0
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tabelVerif.setModel(tableModel);
        tabelVerif.setRowHeight(26);
        tabelVerif.getTableHeader().setReorderingAllowed(false);
    }

    private void loadDataKebutuhan() {
        try {
            daftarKebutuhan = kebutuhanDAO.findKebutuhanMenungguVerifikasi(); // ambil semua yg diajukan
            tableModel.setRowCount(0);

            for (Kebutuhan k : daftarKebutuhan) {
                Sekolah sekolah = k.getSekolah();
                String namaSekolah = sekolah != null ? sekolah.getNamaSekolah() : "-";

                tableModel.addRow(new Object[]{
                        k.getIdKebutuhan(),
                        namaSekolah,
                        k.getNamaAlat(),
                        k.getJumlah() + " unit",
                        k.getPrioritas() != null ? k.getPrioritas().name() : "-",
                        k.getStatus() != null ? k.getStatus().name() : "-"
                });
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data kebutuhan: " + e.getMessage());
            e.printStackTrace();
        }
    }
