    private void lihatDetailBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lihatDetailBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = tabelVerif.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this,
                    "Pilih kebutuhan terlebih dahulu!",
                    "Peringatan",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (daftarKebutuhan == null || daftarKebutuhan.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Data kebutuhan belum dimuat!",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        Kebutuhan kebutuhanDipilih = daftarKebutuhan.get(selectedRow);
        Frame parentFrame = (Frame) SwingUtilities.getWindowAncestor(this);

        DialogDetailKebutuhan dialog = new DialogDetailKebutuhan(parentFrame, true, kebutuhanDipilih);
        dialog.setLocationRelativeTo(this);
        dialog.setVisible(true);

        // Refresh tabel setelah dialog ditutup
        loadDataKebutuhan();
    }//GEN-LAST:event_lihatDetailBtnActionPerformed


