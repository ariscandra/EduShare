    private void loadDataKebutuhan() {
        try {
            if (sekolahLogin == null) {
                JOptionPane.showMessageDialog(this, "Data sekolah belum terdeteksi!");
                daftarKebutuhan = List.of(); // ✅ supaya gak null walau gagal
                return;
            }

            daftarKebutuhan = kebutuhanDAO.findByIdSekolah(sekolahLogin.getIdSekolah());
            tableModel.setRowCount(0);

            for (Kebutuhan k : daftarKebutuhan) {
                tableModel.addRow(new Object[]{
                    k.getIdKebutuhan(),
                    k.getNamaAlat(),
                    k.getJumlah() + " unit",
                    k.getStatus() != null ? k.getStatus().name() : "Belum Terpenuhi",
                    k.getPrioritas() != null ? k.getPrioritas().name() : "-",
                    k.getDeskripsi() == null ? "" : k.getDeskripsi()
                });
            }

        } catch (Exception e) {
            daftarKebutuhan = List.of(); // ✅ kalau error pun gak null
            JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }

