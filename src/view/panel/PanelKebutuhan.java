    private void editKebutuhanBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editKebutuhanBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = tabelKebutuhan.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int idKebutuhan = (int) tableModel.getValueAt(selectedRow, 0);

        try {
            Optional<Kebutuhan> opt = kebutuhanDAO.findById(idKebutuhan);
            if (opt.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Data kebutuhan tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            java.awt.Window parent = SwingUtilities.getWindowAncestor(this);
            DialogTambahKebutuhan dialog = new DialogTambahKebutuhan((java.awt.Frame) parent, sekolahLogin, opt.get());
            dialog.setVisible(true);

            loadDataKebutuhan(); // refresh setelah edit
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_editKebutuhanBtnActionPerformed


