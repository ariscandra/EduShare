    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        int selectedRow = tabelKebutuhan.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this,
                    "Pilih kebutuhan yang ingin dihapus!",
                    "Peringatan",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (daftarKebutuhan == null || daftarKebutuhan.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Daftar kebutuhan belum dimuat!",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        Kebutuhan kebutuhanTerpilih = daftarKebutuhan.get(selectedRow);

        int konfirmasi = JOptionPane.showConfirmDialog(
                this,
                "Apakah Anda yakin ingin menghapus kebutuhan ini?\n"
                + "Nama: " + kebutuhanTerpilih.getNamaAlat(),
                "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE
        );

        if (konfirmasi == JOptionPane.YES_OPTION) {
            try {
                boolean berhasil = kebutuhanDAO.delete(kebutuhanTerpilih.getIdKebutuhan());
                if (berhasil) {
                    JOptionPane.showMessageDialog(this,
                            "Kebutuhan berhasil dihapus!",
                            "Sukses",
                            JOptionPane.INFORMATION_MESSAGE);
                    loadDataKebutuhan(); // refresh tabel
                } else {
                    JOptionPane.showMessageDialog(this,
                            "Gagal menghapus kebutuhan!",
                            "Error",
                            JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,
                        "Terjadi kesalahan: " + e.getMessage(),
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton6ActionPerformed

