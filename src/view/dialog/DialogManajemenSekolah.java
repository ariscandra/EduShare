    /**
     * Load data sekolah dari database
     */
    private void loadSekolahData() {
        if (idSekolah == null) {
            JOptionPane.showMessageDialog(this,
                "ID Sekolah tidak valid",
                "Error",
                JOptionPane.ERROR_MESSAGE);
            dispose();
            return;
        }
        
        try {
            Optional<Sekolah> sekolahOpt = sekolahService.getSekolahById(idSekolah);
            
            if (sekolahOpt.isPresent()) {
                this.sekolahCurrent = sekolahOpt.get();
                populateForm();
            } else {
                JOptionPane.showMessageDialog(this,
                    "Data sekolah tidak ditemukan",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
                dispose();
            }
            
        } catch (Exception e) {
            logger.severe("Error loading sekolah data: " + e.getMessage());
            JOptionPane.showMessageDialog(this,
                "Error loading data: " + e.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE);
            dispose();
        }
    }
    
    /**
     * Populate form dengan data sekolah
     */
    private void populateForm() {
        if (sekolahCurrent == null) return;

        // Populate all fields
        labelNamaSekolah.setText(sekolahCurrent.getNamaSekolah());
        labelNPSN.setText(sekolahCurrent.getNpsn());
        labelJenisSekolah.setText(
            sekolahCurrent.getKategori() != null ? sekolahCurrent.getKategori().toString() : "-"
        );
        labelAlamatSekolah.setText(sekolahCurrent.getAlamatSekolah());
        labelKecamatan.setText(sekolahCurrent.getKecamatan());
        labelKabupaten.setText(sekolahCurrent.getKabupaten());
        labelStatusVerifikasi.setText("Status: " + sekolahCurrent.getStatusVerifikasi());

        String status = sekolahCurrent.getStatusVerifikasi() != null
                ? sekolahCurrent.getStatusVerifikasi().trim()
                : "";

        if (status.equalsIgnoreCase("Terverifikasi")) {
            jButton10.setVisible(false); // sembunyikan tombol
        } else {
            jButton10.setVisible(true);  // tampilkan tombol kalau belum
        }
    }
    
