    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // Implementasi verifikasi sekolah
        try {
            if (sekolahCurrent == null) {
                JOptionPane.showMessageDialog(this,
                        "Tidak ada data sekolah yang dipilih!",
                        "Peringatan",
                        JOptionPane.WARNING_MESSAGE);
                return;
            }

            int konfirmasi = JOptionPane.showConfirmDialog(
                    this,
                    "Apakah Anda yakin ingin memverifikasi sekolah ini?",
                    "Konfirmasi Verifikasi",
                    JOptionPane.YES_NO_OPTION);

            if (konfirmasi == JOptionPane.YES_OPTION) {
                // Update status menjadi "Terverifikasi"
                sekolahCurrent.setStatusVerifikasi("Terverifikasi");
                boolean hasil = sekolahService.updateStatusVerifikasi(
                        sekolahCurrent.getIdSekolah(), "Terverifikasi");

                if (hasil) {
                    JOptionPane.showMessageDialog(this,
                            "Status sekolah berhasil diverifikasi!",
                            "Sukses",
                            JOptionPane.INFORMATION_MESSAGE);
                    dispose(); // tutup dialog
                } else {
                    JOptionPane.showMessageDialog(this,
                            "Gagal memperbarui status sekolah!",
                            "Error",
                            JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Terjadi kesalahan: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton10ActionPerformed

