    private void donasiBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_donasiBtnActionPerformed
        // TODO add your handling code here:
         // Pastikan kebutuhan valid
        if (kebutuhan == null) {
            JOptionPane.showMessageDialog(this, "Data kebutuhan tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Cek user login apakah Donatur
        if (!(user instanceof Donatur)) {
            JOptionPane.showMessageDialog(this, "Hanya Donatur yang bisa melakukan donasi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Donatur donatur = (Donatur) user;

        // Input jumlah donasi
        String inputJumlah = JOptionPane.showInputDialog(this, "Masukkan jumlah donasi (minimal Rp 10.000):", "Input Donasi", JOptionPane.PLAIN_MESSAGE);
        if (inputJumlah == null || inputJumlah.trim().isEmpty()) return;

        BigDecimal jumlahDonasi;
        try {
            // Hilangkan titik, koma, atau simbol rupiah jika ada
            String clean = inputJumlah.replaceAll("[^\\d]", "");
            jumlahDonasi = new BigDecimal(clean);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Nominal donasi tidak valid!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Pilih metode pembayaran
        String[] metodeOptions = {"TRANSFER", "CASH", "QRIS", "LAINNYA"};
        String metode = (String) JOptionPane.showInputDialog(
                this,
                "Pilih metode pembayaran:",
                "Metode Pembayaran",
                JOptionPane.QUESTION_MESSAGE,
                null,
                metodeOptions,
                metodeOptions[0]
        );

        if (metode == null) return;

        // Jalankan proses donasi
        DonasiService donasiService = new DonasiService();
        try {
            DonasiService.DonasiResult result = donasiService.buatDonasi(
                    donatur.getIdPengguna(),
                    kebutuhan.getIdKebutuhan(),
                    jumlahDonasi,
                    metode.toLowerCase().trim()
            );

            if (result.isSuccess()) {
                    JOptionPane.showMessageDialog(this, result.getMessage(), "Sukses", JOptionPane.INFORMATION_MESSAGE);
                    lblTerkumpul.setText("Rp " + donasiService.getTotalDonasiKebutuhan(kebutuhan.getIdKebutuhan()));

                    // âœ… Jalankan callback kalau ada
                    if (onDonasiSelesai != null) {
                        onDonasiSelesai.run();
                    }

                    this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, result.getMessage(), "Gagal", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            donasiService.close();
        }
    }//GEN-LAST:event_donasiBtnActionPerformed

