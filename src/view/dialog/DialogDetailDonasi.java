    private void tolakBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tolakBtnActionPerformed
        // TODO add your handling code here:
        String alasan = JOptionPane.showInputDialog(this, "Masukkan alasan penolakan:");
        if (alasan == null || alasan.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Alasan penolakan wajib diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Apakah Anda yakin ingin menolak donasi ini?",
                "Konfirmasi Penolakan",
                JOptionPane.YES_NO_OPTION
        );

        if (confirm == JOptionPane.YES_OPTION) {
            var result = donasiService.batalkanDonasi(donasi.getIdDonasi(), alasan);
            JOptionPane.showMessageDialog(this, result.getMessage());
            if (result.isSuccess()) {
                statusDonasiField.setText("GAGAL");
                dispose();
            }
        }
    }//GEN-LAST:event_tolakBtnActionPerformed

    private void verifikasiBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verifikasiBtnActionPerformed
        // TODO add your handling code here:
        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Apakah Anda yakin ingin memverifikasi donasi ini?",
                "Konfirmasi Verifikasi",
                JOptionPane.YES_NO_OPTION
        );

        if (confirm == JOptionPane.YES_OPTION) {
            var result = donasiService.konfirmasiDonasi(donasi.getIdDonasi());
            JOptionPane.showMessageDialog(this, result.getMessage());
            if (result.isSuccess()) {
                statusDonasiField.setText("BERHASIL");
                dispose();
            }
        }
    }//GEN-LAST:event_verifikasiBtnActionPerformed

