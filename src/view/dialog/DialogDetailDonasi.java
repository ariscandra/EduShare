    private void setTombolVisibilitas(Donasi donasi) {
        if (donasi.getStatusDonasi() == Donasi.StatusDonasi.berhasil ||
            donasi.getStatusDonasi() == Donasi.StatusDonasi.gagal ||
            donasi.getStatusDonasi() == Donasi.StatusDonasi.dibatalkan) {
            verifikasiBtn.setVisible(false);
            tolakBtn.setVisible(false);
        } else {
            verifikasiBtn.setVisible(true);
            tolakBtn.setVisible(true);
        }

    }

    /**
     * Menampilkan data donasi ke textfield
     */
    private void tampilkanDataDonasi() {
        try {
            if (donasi == null) {
                JOptionPane.showMessageDialog(this, "Data donasi tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Data Donatur
            namaDonaturField.setText(donasi.getDonatur().getNama());
            tanggalDonasiField.setText(donasi.getTanggalDonasi().toString());
            jumlahDiterimaField.setText("Rp " + String.format("%,.0f", donasi.getJumlahDonasi()));

            // Data Sekolah penerima
            Sekolah sekolah = donasi.getKebutuhan().getSekolah();
            if (sekolah != null) {
                namaPenerimaField.setText(sekolah.getNamaSekolah());
                alamatSekolahField.setText(sekolah.getAlamatSekolah());
                kecamatanField.setText(sekolah.getKecamatan());
                kabupatenField.setText(sekolah.getKabupaten());
            }

            // Status donasi
            statusDonasiField.setText(donasi.getStatusDonasi().name().toUpperCase());

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Gagal menampilkan data donasi.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

