    private void lihatDokumenBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lihatDokumenBtnActionPerformed
        // Implementasi view dokumen pendukung
        try {
            List<Dokumen> dokumenList = dokumenService.getDokumenBySekolah(idSekolah);

            if (dokumenList.isEmpty()) {
                JOptionPane.showMessageDialog(this,
                    "Tidak ada dokumen pendukung untuk sekolah ini",
                    "Info",
                    JOptionPane.INFORMATION_MESSAGE);
                return;
            }

            // Ambil dokumen pertama dan buka
            Dokumen dok = dokumenList.get(0);
            String filePath = dok.getPathFile();

            if (filePath != null && !filePath.isEmpty()) {
                File file = new File(filePath);
                if (file.exists()) {
                    Desktop.getDesktop().open(file);
                } else {
                    JOptionPane.showMessageDialog(this,
                        "File dokumen tidak ditemukan: " + filePath,
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this,
                    "Path file tidak valid",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
            logger.severe("Error opening dokumen: " + e.getMessage());
            JOptionPane.showMessageDialog(this,
                "Error membuka dokumen: " + e.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_lihatDokumenBtnActionPerformed

