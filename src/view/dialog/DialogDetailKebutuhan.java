    private void tampilkanData() {
        try {
            if (kebutuhan == null || kebutuhan.getSekolah() == null) {
                JOptionPane.showMessageDialog(this, "Data kebutuhan atau sekolah tidak tersedia!");
                return;
            }

            // Ambil daftar dokumen milik sekolah
            List<Dokumen> dokumenList = dokumenService.getDokumenBySekolah(idSekolah);
            String namaFile = "-";
            String pathFile = null;

            if (dokumenList != null && !dokumenList.isEmpty()) {
                Dokumen dokumen = dokumenList.get(0); // ambil dokumen pertama
                namaFile = getSafeText(dokumen.getNamaFile());
                pathFile = dokumen.getPathFile();
            }

            // Set semua label data
            labelNamaSekolah.setText(getSafeText(kebutuhan.getSekolah().getNamaSekolah()));
            labelNamaAlat.setText(getSafeText(kebutuhan.getNamaAlat()));
            labelJumlah.setText(String.format("%d unit", kebutuhan.getJumlah() != null ? kebutuhan.getJumlah() : 0));
            labelPrioritas.setText(kebutuhan.getPrioritas() != null ? kebutuhan.getPrioritas().name() : "-");
            labelStatus.setText("Status: " + (kebutuhan.getStatus() != null ? kebutuhan.getStatus().name() : "-"));
            labelDeskripsiKebutuhan.setText(getSafeText(kebutuhan.getDeskripsi()));
            labelAlamatSekolah.setText(getSafeText(kebutuhan.getSekolah().getAlamatSekolah()));
            labelJenisSekolah.setText(kebutuhan.getSekolah().getKategori() != null
                    ? kebutuhan.getSekolah().getKategori().toString()
                    : "-");
            labelNPSN.setText(getSafeText(kebutuhan.getSekolah().getNpsn()));
            labelKabupaten.setText(getSafeText(kebutuhan.getSekolah().getKabupaten()));
            labelKecamatan.setText(getSafeText(kebutuhan.getSekolah().getKecamatan()));
            // Set nama file dokumen
            labelNamaFile.setText(namaFile);
            labelNamaFile.putClientProperty("filePath", pathFile); // simpan path-nya ke properti label (buat dipakai nanti)

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                    "Gagal menampilkan data kebutuhan: " + e.getMessage(), 
                    "Error", 
                    JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }


    private String getSafeText(String value) {
        return value != null && !value.trim().isEmpty() ? value : "-";
    }
    
    private void cekStatus() {
        if (kebutuhan.getStatus() == Kebutuhan.Status.diverifikasi
                || kebutuhan.getStatus() == Kebutuhan.Status.ditolak
                || kebutuhan.getStatus() == Kebutuhan.Status.terpenuhi) {
            verifBtn.setVisible(false);
            tolakBtn.setVisible(false);
        }
    }

